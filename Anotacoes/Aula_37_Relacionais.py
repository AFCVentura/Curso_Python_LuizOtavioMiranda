# > MAIOR
# < MENOR
# >= MAIOR OU IGUAL
# <= MENOR OU IGUAL
# == IGUAL
# != DIFERENTE

# Importante lembrar que um = é atribuição e dois == é operador relacional de igualdade
# Há linguagens que usam outras simbologias pro == e pro != tipo o JS que usa == pra igualdade de valor e === pra igualdade de valor e tipo, o python faz igualdade de valor E tipo no == já (como dito em outra aula)

# Sinais de maior e menor são geralmente usados com números mas não é regra (tem como comparar até string mas isso é mais avançado)
# Já sinais de == e != depende, pode ser número e string também
maior = 2>1
menor = 2<3
maior_igual = 2>=2
menor_igual = 2<=2
igual = 2==2
diferente = 2 != '2'



